package br.com.paf.bean;

public class Usuario {
    private int id;
    private String nome;
    private String login;
    private String senha;
    private int tipo;
    private String laudoAtual;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the login
     */
    public String getLogin() {
        return login;
    }

    /**
     * @param login the login to set
     */
    public void setLogin(String login) {
        this.login = login;
    }

    /**
     * @return the senha
     */
    public String getSenha() {
        return senha;
    }

    /**
     * @param senha the senha to set
     */
    public void setSenha(String senha) {
        this.senha = senha;
    }

    /**
     * @return the tipo
     */
    public int getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(int tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the laudoAtual
     */
    public String getLaudoAtual() {
        return laudoAtual;
    }

    /**
     * @param laudoAtual the laudoAtual to set
     */
    public void setLaudoAtual(String laudoAtual) {
        this.laudoAtual = laudoAtual;
    }
    
}
